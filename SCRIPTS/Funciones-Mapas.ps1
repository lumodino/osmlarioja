function Get-WFSCapabilities {
    param(
        #PARAMETRO OBLIGATORIO QUE SEA UNA URL COMO DIRECCION DEL SERVIDOR WFS.
        [Parameter(Mandatory=$True,Position=1)]
        [ValidatePattern(“^(http:\/\/www\.|https:\/\/www\.|http:\/\/|https:\/\/)?[a-z0-9]+([\-\.]{1}[a-z0-9]+)*\.[a-z]{2,5}(:[0-9]{1,5})?(\/.*)?$”)]
        [string]$ServidorWFS,
        #PARAMETRO OPCIONAL PARA OBTENCION DE LOS CAMPOS DE CADA CAPA.
        [Parameter(Mandatory=$False,Position=2)]
        [switch]$MostrarCampos
    )
    #PARAMETROS PARA OBTENCION DE CAPACIDADES DEL SERVIDOR WFS
    $parametrosWFSGetCapabilities = '?service=wfs&request=GetCapabilities'
    #CONSULTA AL SERVIDOR CON LA URL COMPUESTA POR WFS Y PARAMETROS.
    [xml]$resultadoWFSGetCapabilities = Invoke-WebRequest $servidorwfs$parametrosWFSGetCapabilities -UseBasicParsing
    #CREACION OBJETO CON LA INFORMACION PRINCIPAL
    $OBJETOWFS=[pscustomobject]@{NAME=$resultadowfsGetCapabilities.WFS_Capabilities.ServiceIdentification.Title;DESCRIPTION=$resultadowfsGetCapabilities.WFS_Capabilities.ServiceIdentification.Abstract;URLWFS=[STRING]$($servidorwfs);MAXOBJ=[int]$(($resultadoWFSGetCapabilities.WFS_Capabilities.OperationsMetadata.Constraint | Where-Object {$_.name -eq "DefaultMaxFeatures"}).value);VERSIONWFS=$resultadowfsGetCapabilities.WFS_Capabilities.version;OUTPUT=($resultadoWFSGetCapabilities.WFS_Capabilities.OperationsMetadata.Operation.parameter | Where-Object {$_.name -eq "outputformat"}).value | Get-Unique;INPUT=($resultadoWFSGetCapabilities.WFS_Capabilities.OperationsMetadata.Operation.parameter | Where-Object {$_.name -eq "acceptformats"}).value;WFSVERSIONS=($resultadoWFSGetCapabilities.WFS_Capabilities.OperationsMetadata.Operation.parameter | Where-Object {$_.name -eq "acceptVERSIONS"}).value;LAYERS=$resultadowfsGetCapabilities.WFS_Capabilities.FeatureTypeList.FeatureType.name}
    #SE AÑADE AL OBJETO INFORMACION DE CAMPOS POR CADA CAPA
    if($MostrarCampos){
        #REALIZA UNA PETICION HTTP NUEVA POR CADA CAPA.
        $OBJETOWFS.layers | ForEach-Object {$OBJETOWFS | Add-Member -NotePropertyName "$_" -NotePropertyValue $(([xml](Invoke-WebRequest "$($servidorwfs)?service=wfs&version=$($OBJETOWFS.VERSIONWFS)&request=DescribeFeatureType&typename=$($_)&srsname=EPSG:4326&typeNames=namespace:$($_)" -UseBasicParsing)).schema.complexType.complexContent.extension.sequence.element.name)}
        }
    return $OBJETOWFS
}




